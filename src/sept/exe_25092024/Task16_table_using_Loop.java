package sept.exe_25092024;

public class Task16_table_using_Loop {
    public static void main(String[] args) {
        int no1 = 10;

        for (int i = 1; i <= 10; i++)
            {
                System.out.printf("%d * %d = %d", no1, i, no1 * i);
                System.out.println("\n");

            }
        }
    }



// Table of number n = 10, print table with For or while.